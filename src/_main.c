float dt;
extern State stateExample;

int main(void) {
  SM_ChangeState(&stateExample, NULL);

  while (!WindowShouldClose()) {
    dt = GetFrameTime();
    SM_Update(dt);
    SM_Draw();
  }

  return 0;
}

